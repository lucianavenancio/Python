#integers
a = int(1) #a vai ser 1
b = int(2.8) #b vai ser 2
c = int("3") #c vai ser 3
print(a) 
print(b) 
print(c)

#Floats
d = float(1) #d vai ser 1.0
e = float(2.8) #e vai ser 2.8
f = float("3") #f vai ser 3.0
g = float("4.2") #g vai ser 4.2
print(d)
print(e)
print(f)
print(g)

#strings
h = str("s1") #h vai ser 's1'
i = str(2) #i vai ser '2'
j = str(3.0) #j vai ser '3.0'
print(h)
print(i)
print(j)